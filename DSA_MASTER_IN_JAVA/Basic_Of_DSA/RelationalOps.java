package DSA_MASTER_IN_JAVA.Basic_Of_DSA;

public class RelationalOps {
     public static void main(String[] args) {
          // ==, !=, <,>, =<, <=, !

          System.out.println(6==5);
          System.out.println(6<5);
          System.out.println(6>5);
          System.out.println(6<=5);
          System.out.println(6>=5);
          System.out.println(6!=5);

          // Boolean operator
          boolean flag = true;
          boolean flag2 = false;
          System.out.println(flag2+" and "+flag);
          System.out.println(!flag2+" and "+flag);
     }
}
