package DSA_MASTER_IN_JAVA.Method;

public class InbuiltMethod {
     public static void main(String[] args) {
          // Explore most used inbuilt Math method in java

          System.out.println(Math.PI); // it's a value of math
          System.out.println(Math.abs(-12)); // it's a math method gives us +ve value even given -ve value
          System.out.println(Math.cbrt(9)); // it gives us cube root
          System.out.println(Math.ceil(9.9)); // it gives greatest ceil value
          System.out.println(Math.ceil(-9.9)); // it gives greatest ceil value
          System.out.println(Math.floor(-4.5)); // it gives greatest integer value
          System.out.println(Math.max(12,12.4));
          System.out.println(Math.min(12,12.4));
          System.out.println(Math.pow(3, 2));
          System.out.println(Math.random());
          System.out.println(Math.round(3.4));

          // Max of three num
          System.out.println(Math.max(Math.max(3,5), 7));
     }
}
